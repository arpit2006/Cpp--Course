// Built-in Types
// Type                      Keyword
// Boolean               -->  bool (True/False)
// Character             -->  char (1 byte)
// Integer               -->  int (2 byte)
// Floating point        -->  float (4 byte)
// Double floating point -->  double (4 byte)
// Valueless             -->  void (no value)
// String                -->  str (sequence of characters)
// List                  -->  list (sequence of values)
// Tuple                 -->  tuple (sequence of values)
// Dictionary            -->  dict (key-value pairs)
// Set                   -->  set (unordered collection of unique values)
// Complex number        -->  complex (a + bj)
// Byte                  -->  bytes (sequence of integers)


